// Arithmetic operators
var num1 = 10;
var num2 = 5;

var addition = num1 + num2;
var subtraction = num1 - num2;
var multiplication = num1 * num2;
var division = num1 / num2;
var modulus = num1 % num2;
var increment = ++num1;
var decrement = --num2;

console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
console.log("Modulus:", modulus);
console.log("Increment:", increment);
console.log("Decrement:", decrement);

// Comparison operators
var x = 10;
var y = 5;

console.log("Equal:", x == y);
console.log("Not Equal:", x != y);
console.log("Greater Than:", x > y);
console.log("Less Than:", x < y);
console.log("Greater Than or Equal:", x >= y);
console.log("Less Than or Equal:", x <= y);

// Logical operators
var p = true;
var q = false;

console.log("Logical AND:", p && q);
console.log("Logical OR:", p || q);
console.log("Logical NOT:", !p);

// Assignment operators
var a = 10;
var b = 5;

a += b;
console.log("Add and Assign:", a);

a -= b;
console.log("Subtract and Assign:", a);

a *= b;
console.log("Multiply and Assign:", a);

a /= b;
console.log("Divide and Assign:", a);

a %= b;
console.log("Modulus and Assign:", a);

// String concatenation
var firstName = "Saloni";
var lastName = "Kareer";

var fullName = firstName + " " + lastName;
console.log("Full Name:", fullName);
